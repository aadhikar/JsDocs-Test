/**
 * @param  {number} num1 First parameter for addition operation
 * @param  {number} num2 Second parameter for addition operation
 * @return {number} Result
 */
function getSum (num1, num2){
	var addition = num1 + num2;
	return addition;
}
/**
 * @param  {number} num1 First parameter for subtractin operation
 * @param  {number} num2 Second parameter for subtractin operation
 * @return {number} Result
 */
function getDiffernce (num1, num2){
	var subtractin = num1 - num2;
	return subtractin;
}
/**
 * @param  {number} num1 First parameter for multiplication operation
 * @param  {number} num2 Second parameter for multiplication operation
 * @return {number} Result
 */
function getProduct (num1, num2){
	var multiplication = num1 * num2;
	return multiplication;
}/**
 * @param  {number} num1 First parameter for division operation
 * @param  {number} num2 Second parameter for division operation
 * @return {number} Result
 */
function getPortions (num1, num2){

	var division = num1 / num2;
	return division;
}/**
 * @param  {number} num1 First parameter for reminder operation
 * @param  {number} num2 Second parameter for reminder operation
 * @return {number} Result
 */
function getReminder (num1, num2){

	var reminder = num1 % num2;
	return reminder;
}
